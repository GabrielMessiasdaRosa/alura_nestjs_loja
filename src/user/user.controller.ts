import { Body, Controller, Get, Param, Post, Put } from '@nestjs/common';
import { UserService } from './user.service';
import { CreateUserDto } from './dto/create-user.dto';
import { UserEntity } from './user.entity';
import { v4 as uuid } from 'uuid';
import { ListUserDto } from './dto/list-user.dto';
import { UpdateUserDto } from './dto/update-user.dto';
@Controller('/users') // ğŸ‘ˆ Route path @Controller is a decorator that defines a controller that will handle requests for a specific route path.
export class UserController {
  /*  private userService = new UserService(); */ // ğŸ‘ˆ Create an instance of the UserService class

  constructor(private userService: UserService) {} // ğŸ‘ˆ Dependency injection in the constructor to inject the UserService instance into the UserController class.

  @Post() // ğŸ‘ˆ Route handler @Post is a decorator that defines a route handler for POST requests to the route path defined by the @Controller decorator.
  async createUser(@Body() body: CreateUserDto) {
    // ğŸ‘ˆ Route handler method that accepts a CreateUserDto object as the request body.
    // create user
    const userEntity = new UserEntity(); // ğŸ‘ˆ Create an instance of the UserEntity class

    userEntity.name = body.name; // ğŸ‘ˆ Set the name property of the userEntity object to the name property of the CreateUserDto object
    userEntity.email = body.email; // ğŸ‘ˆ Set the email property of the userEntity object to the email property of the CreateUserDto object
    userEntity.password = body.password; // ğŸ‘ˆ Set the password property of the userEntity object to the password property of the CreateUserDto object
    userEntity.id = uuid(); // ğŸ‘ˆ Set the id property of the userEntity object to a unique id generated by the uuid package

    this.userService.createUser(userEntity);
    return {
      // ğŸ‘ˆ return user object;
      user: new ListUserDto(userEntity.id, userEntity.name), // ğŸ‘ˆ return a new ListUserDto object with the id and name properties of the userEntity object
      message: 'User created successfully',
    };
  }

  @Get()
  async getUsers() {
    const savedUsers = await this.userService.getUsers(); // ğŸ‘ˆ Call the getUsers method of the UserService class and assign the result to the savedUsers variable
    const users = savedUsers.map((user) => {
      // ğŸ‘ˆ Map the savedUsers array to a new array of objects with only the id and name properties
      return new ListUserDto(user.id, user.name);
    });
    return users; // ğŸ‘ˆ return users array
  }

  @Put('/:id')
  async updateUser(@Body() body: UpdateUserDto, @Param('id') id: string) {
    const updatedUser = await this.userService.updateUser(id, body);

    return {
      user: new ListUserDto(updatedUser.id, updatedUser.name),
      message: 'User updated successfully',
    };
  }
}
